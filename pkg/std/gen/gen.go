//go:build gen

package main

import (
	"bytes"
	"go/format"
	"html/template"
	"log/slog"
	"os"
	"path/filepath"
	"runtime"

	"golang.org/x/tools/go/packages"
)

//go:generate go run -tags gen github.com/zchee/goimports-rereviser/v4/pkg/std/gen

const (
	fileName = "package_list.go"

	fileTemplate = `// Code generated by ./gen/gen.go DO NOT EDIT.
package std

// StdPackages is a set of go libs
var StdPackages = map[string]struct{}{
{{- range $index, $element := .}}
	"{{$element}}": {},
{{- end}}
}

`
)

var staticPackageList = []string{
	"syscall/js",
}

func main() {
	w := bytes.NewBufferString("")

	tpl := template.New("tpl")
	tpl, err := tpl.Parse(fileTemplate)
	if err != nil {
		slog.Error("failed to parse template", "err", err)
		os.Exit(1)
	}

	packageList, err := packages.Load(nil, "std")
	if err != nil {
		slog.Error("failed to load packages", "err", err)
		os.Exit(1)
	}

	for _, experimentalPackage := range staticPackageList {
		packageList = append(packageList, &packages.Package{
			ID: experimentalPackage,
		})
	}

	if err := tpl.Execute(w, packageList); err != nil {
		slog.Error("failed to execute template", "err", err)
		os.Exit(1)
	}

	data, err := format.Source(w.Bytes())
	if err != nil {
		slog.Error("failed to format source", "err", err)
		os.Exit(1)
	}

	_, filename, _, ok := runtime.Caller(0)
	if !ok {
		slog.Error("get current filename")
		os.Exit(1)
	}
	baseDir := filepath.Dir(filename)

	filePath := filepath.Join(baseDir, "..", fileName)
	slog.Info("file path to be updated", "path", filePath)
	if err := os.WriteFile(filePath, data, 0o644); err != nil {
		slog.Error("failed to write file", "err", err)
		os.Exit(1)
	}
}
